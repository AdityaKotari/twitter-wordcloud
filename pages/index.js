import Head from "next/head"
import { useEffect, useState } from "react"

import router from "next/router"

const Home = () => {
    const [username, setUsername] = useState("")
    

    const handleChange = (e) => {
        e.preventDefault()
        setUsername(e.target.value)
    }
    const handleSubmit = (e) => {
        e.preventDefault()
        router.push(`/stats?username=${username}`)
    }

    return (
        <div>
            <Head>
                <title>Twitter Stats</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main>
                <div>
                    <p className = "hellotext one">Whose Twitter</p>
                    <p className = "hellotext two">Do you want us to find?</p>
                </div>
                <form onSubmit={handleSubmit}>
                    <span className="hello-input-text">Twitter handle: &nbsp;@
                    <input className="hello-input hello-input-text"
                        
                        onChange={handleChange}
                        onPaste={handleChange}
                    />
                    </span>
                </form>
            </main>
        </div>
    )
}

export default Home
